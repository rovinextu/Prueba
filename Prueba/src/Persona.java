public class Persona {

private String nombre;
private String telefono;
private String correo_electronico;

public String getNombre(){
return nombre;
}

public void setNombre(String nombre_catch){
this.nombre=nombre_catch;
}



public String getTelefono(){
return telefono;
}

public void setTelefono(String tel_catch){
this.telefono=tel_catch;
}




public String getCorreo(){
return correo_electronico;
}

public void setCorreo(String correo_catch){
this.correo_electronico=correo_catch;
}



}